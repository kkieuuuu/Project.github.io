CREATE TABLE TINHTHANH (
    MaTinh VARCHAR2(6),
    TenTinh VARCHAR2(25),
    CONSTRAINT PK_TINHTHANH_MaTinh PRIMARY KEY (MaTinh)
);

CREATE TABLE TRAMTHUPHIBOT ( 
    MaTram VARCHAR2(6),
    TenTram VARCHAR2(25),
    DiaChi VARCHAR2(100),
    MaTinh VARCHAR2(6),
    TuyenDuong VARCHAR2(50),
    CONSTRAINT PK_TRAMTHUPHIBOT_Manv PRIMARY KEY (MaTram),
    CONSTRAINT FK_TRAMTHUPHIBOT_MaTinh FOREIGN KEY (MaTinh) REFERENCES TINHTHANH
);

CREATE TABLE CHUCVU ( 
    MaCV VARCHAR2(6),
    TenCV VARCHAR2(25),
    CONSTRAINT PK_CHUCVU_MaCV PRIMARY KEY (MaCV)
);

CREATE TABLE NHANVIEN( 
    MaNV VARCHAR2(6),
    HoNV VARCHAR2(20),
    TenNV VARCHAR2(25),
    GioiTinhNV VARCHAR2(25),
    DiaChiNV VARCHAR2(100),
    EmailNV VARCHAR2(25),
    SDTNV VARCHAR2(20),
    NgaySinhNV DATE,
    NgayVaoLam DATE,
    Luong  NUMBER(12),
    CCCDNV VARCHAR2(20),
    MaCV VARCHAR2(6),
    MaTram  VARCHAR2(6),
    CONSTRAINT PK_NhanVien_Manv PRIMARY KEY (MaNV),
    CONSTRAINT NhanVien_GioiTinh CHECK (GioiTinhNV IN ('Nam', N'Nữ')),
    CONSTRAINT NhanVien_Luong CHECK (Luong > 0),
    CONSTRAINT NhanVien_Email UNIQUE (EmailNV),
    CONSTRAINT NhanVien_CCCD UNIQUE (CCCDNV),    
    CONSTRAINT FK_NhanVien_MaCV FOREIGN KEY (MaCV) REFERENCES CHUCVU,
    CONSTRAINT FK_NhanVien_MaTram FOREIGN KEY (MaTram) REFERENCES TRAMTHUPHIBOT
);

CREATE TABLE PHUONGTIENTHUPHI(
    MaLoaiXe VARCHAR2(6),
    GhiChu NVARCHAR2(150)	,
    CONSTRAINT PK_PHUONGTIENTHUPHI_MaLoaiXe PRIMARY KEY (MaLoaiXe)
);

CREATE TABLE KHACHHANG(
    MaKH VARCHAR2(6),
    HoKH VARCHAR2(20),
    TenKH VARCHAR2(25),
    NgaySinhKH DATE,
    GioiTinhKH VARCHAR2(25),
    DiaChiKH VARCHAR2(100),
    EmailKH VARCHAR2(25),
    SDTKH VARCHAR2(20),
    CCCDKH VARCHAR2(20),
    CONSTRAINT PK_KhachHang_MaKH PRIMARY KEY (MaKH),
    CONSTRAINT KhachHang_GioiTinh CHECK (GioiTinhKH IN ('Nam', N'Nữ')),
    CONSTRAINT KhachHang_Email UNIQUE (EmailKH),
    CONSTRAINT KhachHang_CCCDKH UNIQUE (CCCDKH)
);

CREATE TABLE XE(
    MaXe VARCHAR2(6),
    BienSoXe VARCHAR2(20),
    MaKH VARCHAR2(6),
    MaLoaiXe VARCHAR2(6),
    CONSTRAINT PK_XE_MaXe PRIMARY KEY (MaXe),
    CONSTRAINT XE_BienSoXe UNIQUE (BienSoXe),  
    CONSTRAINT FK_XE_MaKH FOREIGN KEY (MaKH) REFERENCES KHACHHANG,
    CONSTRAINT FK_XE_MaLoaiXe FOREIGN KEY (MaLoaiXe) REFERENCES PHUONGTIENTHUPHI
