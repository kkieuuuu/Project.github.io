CREATE TABLE TAIKHOAN(
    MaTK VARCHAR2(6),
    MatKhau VARCHAR2(30),
    Quyen NUMBER(6),
    MaNV VARCHAR2(6),
    CONSTRAINT PK_TAIKHOAN_MaTK PRIMARY KEY (MaTK),
    CONSTRAINT FK_TAIKHOAN_MaNV FOREIGN KEY (MaNV) REFERENCES NHANVIEN
);

CREATE TABLE LOAIVE(
    MaLoaiVe VARCHAR2(6),
    TenLoaiVe NVARCHAR2(50),
    CONSTRAINT PK_LOAIVE_MaLoaiVe PRIMARY KEY (MaLoaiVe)
);

CREATE TABLE THEETAG(
    MaVachThe VARCHAR2(6),
    MaKH VARCHAR2(6),
    MaLoaiVe VARCHAR2(6),
    MaXe VARCHAR2(6),
    SoDuTK NUMBER(12),
    CONSTRAINT PK_THEETAG_MaVachThe PRIMARY KEY (MaVachThe),
    CONSTRAINT FK_THEETAG_MaLoaiVe FOREIGN KEY (MaLoaiVe) REFERENCES LOAIVE,
    CONSTRAINT FK_THEETAG_MaXE FOREIGN KEY (MaXe) REFERENCES XE,
    CONSTRAINT FK_THEETAG_MaKH FOREIGN KEY (MaKH) REFERENCES KHACHHANG
