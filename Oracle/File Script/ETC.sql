/*
Tạo thủ tục có tên proc_update_slr cập nhật lương của nhân viên với diều kiện nhân viên nào làm việc trên 2 năm thì tăng lương thêm 2000000, nhân viên làm việc trên 1 năm và duới 2 năm thì tăng lương thêm 1500000, nhân viên nào làm việc đúng 1 năm thì tăng 1000000, còn lại không tăng.
*/
CREATE OR REPLACE PROCEDURE proc_update_slr 
AS
    v_sonam VARCHAR2(2);
    CURSOR c_nhanvien IS 
        SELECT * FROM NHANVIEN;
BEGIN 
    FOR r_nhanvien IN c_nhanvien
    LOOP
        v_sonam := to_char(sysdate,'yyyy') - to_char(r_nhanvien.NGAYVAOLAM,'yyyy');
        IF v_sonam >= 2 
        THEN 
            UPDATE NHANVIEN 
            SET LUONG = LUONG+2000000
            WHERE MaNV = r_nhanvien.MaNV;
            DBMS_OUTPUT.PUT_LINE('Nhân viên: ' || r_nhanvien.TENNV|| ' đã được tăng lương thêm 2.000.000');
        ELSIF v_sonam > 1 
        THEN 
            UPDATE NHANVIEN 
            SET LUONG = LUONG+1500000
            WHERE MaNV = r_nhanvien.MaNV;
            DBMS_OUTPUT.PUT_LINE('Nhân viên: ' || r_nhanvien.TENNV|| ' đã được tăng lương thêm 1.500.000');        
        ELSIF v_sonam = 1
        THEN 
            UPDATE NHANVIEN 
            SET LUONG = LUONG+1000000
            WHERE MaNV = r_nhanvien.MaNV;
            DBMS_OUTPUT.PUT_LINE('Nhân viên: ' || r_nhanvien.TENNV|| ' đã được tăng lương thêm 1.000.000');        
        ELSE 
            DBMS_OUTPUT.PUT_LINE('Các nhân viên còn lại không được tăng');
        END IF;
    END LOOP; 
END;
--Kiểm thử
SET SERVEROUTPUT ON
DECLARE
BEGIN
    proc_update_slr; 
END;




