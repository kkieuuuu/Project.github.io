Thông tin hiển thị gồm: mã hóa đơn, ngày lập, mã vạch thẻ.
Nếu không có hóa đơn nào được lập trong thời gian truyền vào thì in ra thông báo "Không có hóa đơn nào được lập trong thời gian này"
Xử lý ngoại lệ: Ngày bắt đầu phải nhỏ hơn ngày kết thúc.
*/
CREATE OR REPLACE PROCEDURE PROC_THONGKE_HOADON
    (v_ngaybd HOADON.NGAYLAP%TYPE,
    v_ngaykt HOADON.NGAYLAP%TYPE)
IS
    my_ex EXCEPTION;
    CURSOR c_thongke_cur IS
        SELECT MaHD, MAVACHTHE, NgayLap
        FROM HOADON
        WHERE NGAYLAP BETWEEN v_ngaybd AND v_ngaykt;
    v_row c_thongke_cur%ROWTYPE;
    v_dem int := 0;
BEGIN
    IF v_ngaybd > v_ngaykt
    THEN 
        RAISE my_ex;
    END IF;
    OPEN c_thongke_cur;
    LOOP    
        FETCH c_thongke_cur INTO v_row;
        EXIT WHEN c_thongke_cur%NOTFOUND;
            DBMS_OUTPUT.PUT_LINE('Mã hóa đơn: '|| v_row.MaHD || 
            ', Ngày lập: '|| to_char(v_row.Ngaylap, 'dd/MM/yyyy') || 
            ', Mã vạch thẻ: '|| v_row.MaVachThe);
            v_dem := v_dem +1;
    END LOOP;
   
    CLOSE c_thongke_cur;
    IF v_dem = 0
    THEN
        DBMS_OUTPUT.PUT_LINE('Không có hóa đơn nào được lập trong thời gian này');
    END IF;
    EXCEPTION 
        WHEN my_ex THEN 
        DBMS_OUTPUT.PUT_LINE('Ngày bắt đầu phải nhỏ hơn ngày kết thúc');
END;
