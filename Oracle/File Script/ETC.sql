CREATE TABLE VIPHAM(
    MaViPham VARCHAR2(6),
    TenViPham NVARCHAR2(150),
    MucPhat VARCHAR2(12),
    CONSTRAINT PK_VIPHAM_MaViPham PRIMARY KEY (MaViPham)
