/*
Viết thủ tục có tên proc_recharge cho phép nạp tiền vào thẻ Etag có các tham số mã vạch của thẻ Etag, số tiền cần nạp vào.
Xử lý ngoại lệ: Kiểm tra mã vạch thẻ có tồn tại trong bảng THEETAG hay không? 
Nếu không có in ra thông báo lỗi. Viết khối lệnh PL/SQL thực thi thủ tục.
*/
CREATE OR REPLACE PROCEDURE proc_recharge
    (v_mavachthe IN THEETAG.MAVACHTHE%TYPE,
    v_sotiennap IN THEETAG.SODUTK%TYPE)
IS
    CURSOR theetag_cur IS 
        SELECT * 
        FROM THEETAG;
    v_sodu  THEETAG.SODUTK%TYPE;
    MY_EX EXCEPTION;
BEGIN
    FOR v_rec IN theetag_cur
    LOOP
        IF v_rec.MaVachThe = v_mavachthe
            THEN  v_sodu := v_rec.SODUTK + v_sotiennap;
        ELSE
            RAISE MY_EX;
        END IF;
    END LOOP;
    UPDATE THEETAG 
    SET SODUTK = v_sodu
    WHERE  MAVACHTHE = v_mavachthe;
    EXCEPTION 
    WHEN my_ex THEN
    DBMS_OUTPUT.PUT_LINE('Không có mã vạch thẻ');
END;

